def build_screen_regions(x, y, w, h):
    return {
        "全界面": (x, y, w, h),
        "左上": (x, y, w // 2, h // 2),
        "右上": (x + w // 2, y, w // 2, h // 2),
        "左下": (x, y + h // 2, w // 2, h // 2),
        "右下": (x + w // 2, y + h // 2, w // 2, h // 2),
        "上": (x, y, w, h // 2),
        "下": (x, y + h // 2, w, h // 2),
        "左": (x, y, w // 2, h),
        "右": (x + w // 2, y, w // 2, h),
        "中间": (x + w // 4, y + h // 4, w // 2, h // 2),
    }
